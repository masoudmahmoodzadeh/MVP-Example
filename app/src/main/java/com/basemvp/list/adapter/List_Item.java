package com.basemvp.list.adapter;

public class List_Item {

    private int icon;
    private String title;

    public List_Item(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }


}
